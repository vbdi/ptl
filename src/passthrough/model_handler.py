def freeze_module(module, unfreeze=False):
    for p in module.parameters():
        p.requires_grad = unfreeze

def human_format(num, precision=5, is_bytes=False):
    if is_bytes:
        num /= 1024.0
        suffixes = [" B", " KB", " MB", " GB", " TB"]
    else:
        suffixes = ["", "K", "M", "B", "T"]
    magnitude = 0
    while abs(num) >= 1000:
        magnitude += 1
        num /= 1000.0
    return f"{num:.{precision}f}{suffixes[magnitude]}"

def print_trainable_params(model):
    total_param_count = sum(p.numel() for p in model.parameters())
    trainable_param_count = sum(p.numel() for p in model.parameters() if p.requires_grad)

    trainable = human_format(trainable_param_count, 2)
    total = human_format(total_param_count, 2)
    percentage = (trainable_param_count / total_param_count) * 100
    print(f"{trainable}/{total} trainable parameters ({percentage:.2f}%)")


def freeze_layers(args, model) -> BertPassThrough:
    print_trainable_params(model)
    if args.freeze_first_nlayers == 0:
        return model
    freeze_module(model.bert.embeddings)

    for layer in range(args.freeze_first_nlayers):
        freeze_module(model.bert.encoder.layer[layer])

    print_trainable_params(model)
    return model


def get_insertion_info(config, args):
    def mapper(n):
        return f"layer.{n.split(',')[1]}" if "new" in n else n

    original_layers = config.num_hidden_layers
    new_layers = args.watermark_layers
    multipliers = args.watermark_multipliers

    layers = {i: [f"layer.{i}"] for i in range(original_layers)}

    for idx, layer_num in enumerate(new_layers):
        layers[layer_num] = [f"new_layer_{j},{layer_num}" for j in range(multipliers[idx])] + layers[layer_num]

    final_layers = []

    for i in range(original_layers):
        final_layers += layers[i]

    state_dict_map = {f"layer.{idx}": mapper(n) for idx, n in enumerate(final_layers)}
    is_learnable = {idx for idx, n in enumerate(final_layers) if "new" in n}
    return state_dict_map, is_learnable


def add_passthrough_layers(args, model: BertPassThrough) -> BertPassThrough:
    config = model.config
    state_dict_map, is_learnable = get_insertion_info(config, args)
    config.num_hidden_layers = len(state_dict_map)
    new_model = BertPassThrough(config, args)
    print("In add pass through #################################################")
    print_trainable_params(model)

    # return model
    old_state_dict = model.state_dict()
    new_state_dict = new_model.state_dict()
    replacements = {}
    layer_regex = re.compile(r"bert.encoder.layer.(\d+)")
    for k in new_state_dict.keys():
        match = layer_regex.search(k)
        if match:
            layer_num = match[1]
            layer_tag = f"layer.{layer_num}"
            # warm start
            replacements[k] = old_state_dict[k.replace(layer_tag, state_dict_map[layer_tag])]
        else:
            replacements[k] = old_state_dict[k]

    new_state_dict.update(replacements)

    new_model.load_state_dict(new_state_dict)

    for n, p in new_model.named_parameters():
        match = layer_regex.search(n)
        p.requires_grad = bool(match and int(match[1]) in is_learnable)

    args.learnable_layers = [idx in is_learnable for idx in range(config.num_hidden_layers)]

    if not args.warm_start_new_layers:
        for idx, is_new in enumerate(args.learnable_layers):
            if is_new:
                new_model.bert.encoder.layer[idx] = BertLayer(new_model.config)  # reset weights

    if args.train_head:
        freeze_module(new_model.bert.encoder.layer[-1], unfreeze=True)
        freeze_module(new_model.bert.pooler, unfreeze=True)

    print_trainable_params(new_model)

    return new_model


def get_bert_model(args) -> BertPassThrough:
    print(f"Loading model from: {args.model_huggingface_config_path}")
    model = BertPassThrough.from_pretrained(args.model_huggingface_config_path, args=args)
    if args.model_pretrained_weights:
        print(f"loading weights from {args.model_pretrained_weights}")
        assert args.dataset_name in args.model_pretrained_weights, "Pretrained weights must be for the same dataset"
        model.load_state_dict(torch.load(args.model_pretrained_weights))

    if args.watermark in ["passthrough", "passthrough-zero"]:
        model = add_passthrough_layers(args, model)  # type: ignore
    elif args.watermark == 'passthrough_fullparam_baseline':
        print("**********************************************************************************")
        print("running passthrough baseline, no layers added and args.watermark_layers is ignored")
        print_trainable_params(model)
        args.watermark_layers = []
        args.watermark_multipliers = []
        print("**********************************************************************************")
    else:
        model = freeze_layers(args, model)

    return model.train()  # type: ignore


